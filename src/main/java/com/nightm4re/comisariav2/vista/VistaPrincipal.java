        int cuenta = 0;
        for (Component componente : panel.getComponents()) {
            if (cuenta < fillerstoremove) {
                if (componente instanceof Box.Filler) {
                    System.out.println("ENTRA");
                    if (componente.getName() == null) {
                        panel.remove(componente);
                        cuenta++;
                    }

                }
            }

        }

